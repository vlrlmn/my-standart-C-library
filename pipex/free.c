#include "pipex.h"

void	free1(char **p)
{
	int	i;

	if (p)
	{
		i = -1;
		while (p[++i])
		{
			free(p[i]);
		}
		free(p);
		p = NULL;
	}
}

void	free2(char **p1, char **p2)
{
	free1(p1);
	free1(p2);
}
