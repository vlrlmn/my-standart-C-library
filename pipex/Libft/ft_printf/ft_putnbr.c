#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	i;

	i = 1;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		i++;
		n = -n;
	}
	if (n > 9)
		i += ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
	return (i);
}
